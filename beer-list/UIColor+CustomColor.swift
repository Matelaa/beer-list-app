import UIKit

extension UIColor {
    static var lightBlue: UIColor {
        return UIColor(red: 0.29, green: 0.56, blue: 0.89, alpha: 1.00)
    }
    
    static var darkBlue: UIColor {
        return UIColor(red: 0.16, green: 0.31, blue: 0.49, alpha: 1.00)
    }
    
    static var lightGray: UIColor {
        return UIColor(red: 0.40, green: 0.40, blue: 0.40, alpha: 1.00)
    }
    
    static var primaryWhite: UIColor {
        return UIColor(red: 1.00, green: 1.00, blue: 1.00, alpha: 1.00)
    }
    
    static var secondaryWhite: UIColor {
        return UIColor(red: 0.96, green: 0.96, blue: 0.96, alpha: 1.00)
    }
}
